#Basic Caltulator

class add:
    def __init__(self):
        pass

    def add(self, a, b):
        return a + b


class subtract:
    def __init__(self):
        pass

    def subtact(self, a, b):
        return a - b

class devide:
    def __init__(self):
        pass

    def devide(self, a, b):
        return a/b


class multiply:
    def __init__(self):
        pass

    def multiply(self, a, b):
        return a*b


